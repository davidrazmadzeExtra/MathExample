import Foundation

class MathManager {
  
  func addNumbers(_ x: Int, _ y: Int) -> Int {
    return x + y
  }
  
  func subtractNumbers(_ x: Int, _ y: Int) -> Int {
    return x - y
  }
  
  func multiplyNumbers(_ x: Int, _ y: Int) -> Int {
    return x * y
  }
  
  func divideNumbers(_ x: Int, _ y: Int) -> Int {
    return x / y
  }
  
}
